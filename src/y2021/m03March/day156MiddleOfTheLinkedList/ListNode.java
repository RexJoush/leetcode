package y2021.m03March.day156MiddleOfTheLinkedList;

/**
 * @author Rex Joush
 * @time 2021.03.12
 */

public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int x) {
        val = x;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }

}
